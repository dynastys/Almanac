package com.xy.xylibrary.ui.activity.login;

public class Exit {

    /**
     * code : 0
     * msg : 清除当前用户所有操作
     * data : true
     */

    private int code;
    private String msg;
    private boolean data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public boolean isData() {
        return data;
    }

    public void setData(boolean data) {
        this.data = data;
    }
}
