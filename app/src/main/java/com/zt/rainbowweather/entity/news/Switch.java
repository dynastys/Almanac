package com.zt.rainbowweather.entity.news;

public class Switch {

    /**
     * code : 200
     * date : true
     * msg : sdk广告开启---
     */

    private int code;
    private boolean date;
    private String msg;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public boolean isDate() {
        return date;
    }

    public void setDate(boolean date) {
        this.date = date;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
